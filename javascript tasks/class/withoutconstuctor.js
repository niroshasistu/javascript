class Student {
    // properties
    id;
    name;
}

// creating object
let s1 = new Student();

// accessing properties
s1.id = 101;
s1.name = "Nirosha";

console.log("Student ID:", s1.id);
console.log("Student Name:", s1.name);
// Output:
// Student ID: 101
// Student Name: Nirosha